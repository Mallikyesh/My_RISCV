   li   x10, 0                   # x10 = i
outer:
   bge  x10, x28, vote_step
   add  x11, x10, zero           # x11 = min_idx
   slli x12, x10, 3
   add  x13, x1, x12
   lw   x14, 0(x13)              # min_dist
